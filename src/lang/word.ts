// lang/word.ts — ЕДИНОЕ определение «словесного символа» для нормализации и карты.
// Юникод-осознанное: буквы и цифры любого алфавита плюс '_' (не только ASCII \w),
// иначе `int число` слипалось бы в `intчисло`, а границы токенов в build-map
// считались бы по другому правилу, чем канон.
const WORD = /[\p{L}\p{N}_]/u;

export function isWordChar(ch: string): boolean {
  return WORD.test(ch);
}
